class ChatMessage(BaseModel):
    """
    A single turn in a restaurant operator's conversation with the agent.
    """

    role: str  # TODO: narrow to Literal["system", "user", "assistant", "tool"]
    """Message role as expected by the GLM chat API."""

    content: str | list
    """Message text or multimodal content list."""

    tool_call_id: str | None = None
    """Present only on role='tool' messages to correlate with the tool call."""


class RestaurantContext(BaseModel):
    """
    Restaurant-scoped context injected into every agent run.

    Loaded from PostgreSQL (db/repositories.py, Section 6) and passed through the LangGraph AgentState so every node has restaurant context available.

    """

    restaurant_id: str
    """Immutable restaurant identifier. Used as tenant_id in DB queries."""

    name: str
    """Restaurant display name for log messages and agent prompts."""

    known_suppliers: list[SupplierInfo] = Field(default_factory=list)
    """Pre-loaded supplier profiles for this restaurant (inference hints)."""

    cuisine_type: str | None = None
    """Cuisine category (e.g. 'Japanese', 'Italian'). Used for ingredient inference."""
